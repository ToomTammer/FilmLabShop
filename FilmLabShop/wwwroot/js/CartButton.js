function increasebtn1() {
    var count = parseInt(document.getElementById('Text1').value);

    if (count >= 100) {
        count = 100
        document.getElementById('Text1').value = count;
    }
    else {
        count = isNaN(count) ? 0 : count;
        count++;
        document.getElementById('Text1').value = count;
        
    }
    
}

